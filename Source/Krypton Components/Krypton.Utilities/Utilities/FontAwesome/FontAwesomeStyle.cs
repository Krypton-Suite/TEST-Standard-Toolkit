    /// Font Awesome Brands style (fab).
    /// </summary>
    Brands,

    /// <summary>
    /// Font Awesome Light style (fal) - Pro only.
    /// </summary>
    Light,

    /// <summary>
    /// Font Awesome Thin style (fat) - Pro only.
    /// </summary>
    Thin,

    /// <summary>
    /// Font Awesome Duotone style (fad) - Pro only.
    /// </summary>
    Duotone
}

